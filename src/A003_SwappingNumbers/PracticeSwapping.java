package A003_SwappingNumbers;

public class PracticeSwapping {

	public static void main(String[] args) {
		
		int a = 20;
		
		int b = 40;
		
		System.out.println("Before Swapping: A is "+a+" B is "+b);
		
		System.out.println();
		
	int c = a;  // c = 20;
		a = b;  // a = 40;
		b = c;	// b = 20; 
		
		System.out.println("After Swapping: A is "+ a+" B is "+b);
		

	}

}
